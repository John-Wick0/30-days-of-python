# Day 1 - 30DaysPython Challenge

print(2 + 3)    # Addition(+)
print(3 - 1)    # Subtraction(-)
print(2 * 3)    # Multiplication(*)
print(3 / 2)    # Division(/)
print(3 ** 2)   # Exponentiation(**)
print(3 % 2)    # Modulus(%)
print(3 // 2)   # Floor division operator(//)

# Checking data types

print(type(10))          # Int
print(type(3.14))        # Float
print(type(1 + 1j))      # Complex
print(type('You been pwnd'))    # String
print(type(True)) # bool
print(type(False))  # bool
print(type([1, 2,3,4]))   #  List
print(type({'name':'Dr.Fan', 'age':35, 'is_married': False})) # Dictionary
print(type({1,2})) # Tuple
print(type(zip([1,2,3],[3,4]))) # Set